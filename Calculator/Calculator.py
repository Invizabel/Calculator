def addition(x, y):
    return x + y

def cube(x):
    return x * x * x

def cube_root(x):
    return x ** (1/3)

def division(x, y):
    solution = 0
    
    if y == 0:
        solution = "undefined"

    else:
        solution = x / y

    return solution

def exponent(x, y):
    return x ** y

def multiplication(x, y):
    return x * y

def square(x):
    return x * x

def square_root(x):
    return x ** (1/2)

def subtraction(x, y):
    return x - y
